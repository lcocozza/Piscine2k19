#include "ft_do_op.h"

int		ft_div(int a, int b)
{
	return (a / b);
}

int		ft_mod(int a, int b)
{
	return (a % b);
}

int		ft_multi(int a, int b)
{
	return (a * b);
}

int		ft_sous(int a, int b)
{
	return (a - b);
}

int		ft_add(int a, int b)
{
	return (a + b);
}
